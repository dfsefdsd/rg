package cn.org.rapid_framework.generator;

public class TC {
	public Object obj;
	public int times;
	
	public TC(Object obj,int times){
		this.obj=obj;
		this.times=times;
	}
	public Object getObj() {
		return obj;
	}
	public void setObj(Object obj) {
		this.obj = obj;
	}
	public int getTimes() {
		return times;
	}
	public void setTimes(int times) {
		this.times = times;
	}
}
